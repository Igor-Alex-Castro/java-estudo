package OrientacaoObjetos;

public class Construtores {
	/* Construtor
	 * 
	 * É uma operação espercial da classe, que executa no momento da instanciação do objeto
	 * 
	 * 
	 * Usos comuns
	 * 		Iniciar valores dos atributos
	 * 		Permitir ou obrigar que o ojeto receba dados/ dependencias no momento da usa instanciação (injeção de dep)
	 * 
	 * Se um construtor customizado não for especificado, a classe disponibiliza o constutor padrao;
	 * 
	 * É possivel especificar mais de um construtor na mesma classe
	 * */
}
