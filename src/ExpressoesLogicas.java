public class ExpressoesLogicas {

	public static void main(String[] args) {
		
		//expressões Logico
		
		//expressões => resultado => valor verdade
		
		//5>10 => falso
		
		/* OPERADORES LOGICO
		 * && E
		 * || OU
		 * ! NÃO
		 */
		
		// E = todas as condições devem ser verdadeiras para resultar em true
		// OU =  Pelo menos uma das condições deve ser verdadeira para resultar em true
		// ! = inverte o valor, se é verdadeiro vira falso, vice e versa.
		
		
		/* ---- TABELA VERDADE-----
				A | B | A && B
		    	F | F |   F
		    	F | V |   F 
		    	V | F |	  F
		    	V | V |   V
		*/
		
		/* ---- TABELA VERDADE-----
				A | B | A || B
    			F | F |   F
    			F | V |   V
    			V | F |	  V
    			V | V |   V 
		 	*/
		
	}

}
